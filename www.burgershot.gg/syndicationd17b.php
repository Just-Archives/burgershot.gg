<br />
Última actualización: 02/04/2019<br />
<span style="font-weight: bold;" class="mycode_b">Arreglos destacados:</span><ul class="mycode_list"><li>El retardo de cuadro de 14ms ha sido removido. Como resultado, el juego se bloquea a 30 FPS en vez de 25 FPS<br />
</li>
<li>Limitador de Cuadros por Segundo (LCP) más preciso, reduciendo un poco los picos de lag cuando se juega con el LCP activado<br />
</li>
<li>Los temporizadores del juego funcionan de forma más precisa, lo que hace que no se congele cuando la tasa de cuadros supera los 1000 FPS; en otras palabras, esto corrige los bloqueos ocasionales en los fadeouts si se juega con LCP desactivado<br />
</li>
<li>El mouse no debe bloquearse aleatoriamente al salir del menú en los SO más nuevos<br />
</li>
<li>La sensibilidad vertical del ratón coincide con la sensibilidad horizontal<br />
</li>
<li>El eje vertical del mouse no se bloquea durante los fadeins de cámara<br />
</li>
<li>NUM5 es vinculable<br />
</li>
<li>Resoluciones 16:9 están disponibles<br />
</li>
<li>Si el archivo de configuración no existe (gta_sa.set), el juego tendrá la resolución predeterminada del escritorio en lugar de 800x600x32<br />
</li>
<li>La dependencia de DirectPlay fue removida - esto debería mejorar la compatibilidad con versiones de Windows 8 y posteriores<br />
</li>
<li>Las sapilcaduras de EAX/NVIDIA fueron removidas (se pueden activar o desactivar; desactivado por defecto)<br />
</li>
<li>Los subtítulos y los textos de la Radio fueron cambiados a la versión de Steam (esto se puede descativar)<br />
</li>
<li>El color del nombre del área ahora coincide con el color de la bando que domina el territorio (Los Santos solamente) (se puede activar o desactivar: desactivado por defecto)<br />
</li>
<li>Los reflejos de las carreteras mojadas se renderizan correctamente (al igual que con Road Reflections Fix)<br />
</li>
<li>Arreglado el efecto del reflejo del Sol en GPUs de AMD e Intel<br />
</li>
<li>Se corrigió un problema que presentaba artefactos gráficos de animaciones de ped con un alto uso de RAM, también llamado "streaming memory bug"<br />
</li>
<li>Las hélices de los helicópteros y aviones se muestran correctamente. Ahora tienen un efecto borroso presente en VC y la version de PS2 de SA<br />
</li>
<li>Las ruedas traseras dobles ahora se muestran correctamente (Yosemite, Cisterna, etc.)<br />
</li>
<li>Las armas son visibles cuando se ven a través de la ventana de un vehículo<br />
</li>
<li>Los vehículos destruidos están correctamente coloreados y ya no brillan<br />
</li>
<li>Los vehículos sucios ahora pueden limpiarse<br />
</li>
<li>Cada vehículo tiene una matrícula única<br />
</li>
<li>Las matrícula ahora se filtran bilinealmente, lo que resulta en una apariencia más suave<br />
</li>
<li>Las partes separadas del vehículo se renderizan desde ambos lados<br />
</li>
<li>Las fases lunares se muestran correctamente (solo cuando se juega con el modo de color de 32-bit)<br />
</li>
<li>Las luces que se reflejan en vehículos y personajes lo hacen de forma correcta<br />
</li>
<li>Además, cuando se juega en calidad media-muy alta, el juego ahora reflejará hasta 6 luces en cada modelo tanto en interiores como en exteriores (en calidad baja, el comportamiento del juego se ha mantenido, permitiendo hasta 4 luces por modelo en exteriores y 6 en el interior)<br />
</li>
<li>Los destellos de disparos de armas ahora se ven mejor<br />
</li>
<li>Temple y Queens ahora son mencionados correctamente en la radio policial<br />
</li>
<li>Viajar lejos del mapa ya no activará el de territorios de bandos extras, ni cancelará la misión de Taxista<br />
</li>
<li>Una falla del Gimnasio ("Has entrenado lo suficiente..." mostrándose infinitamente) ha sido arreglado<br />
</li>
<li>La bonificación por acrobacia Cuadruple funciona correctamente<br />
</li>
<li>Las puertas de los aviones no se rompen después de que el avión se ha reparado.<br />
</li>
<li>La reparación de un avión ahora restablecerá sus accesorios móviles a un estado no dañado<br />
</li>
<li>Varios componentes del vehículo (en particular, el parachoques delantero de Rumpo y el puntal móvil de Bandito) no permanecerán dañados después de que el vehículo haya sido reparado.<br />
</li>
<li>Armas y un jetpack ahora proyectan sombras adecuadas.<br />
</li>
<li>Ya no desaparece el ícono del arma cuando se apunta estando en un jetpack<br />
</li>
<li>Varios contadores de estadísticas ahora se reinician cuando se empieza un nuevo juego, por lo que el jugador no subirá de nivel más rápido después de iniciar el juego nuevo desde una partida guardada<br />
</li>
<li>Se ha solucionado el error que hace que las prostitutas se queden quietas durante la solicitud<br />
</li>
<li>Los cuadros de texto ahora se pueden mostrar junto con el texto de Mission Passed<br />
</li>
<li>El reflejo del espejo ya no se rompe con el anti-aliasing habilitado<br />
</li>
<li>La opción de anti-aliasing ha sido modificada: en lugar de incluir 1, 2, 3 opciones (que en realidad son 1x/2x/4x MSAA), el juego ahora mostrará los valores adecuados de MSAA desde 2x hasta 16x (dependiendo del nivel máximo de MSAA admitido por la tarjeta gráfica)<br />
</li>
<li>Chocar con otro carro ahora dañará las partes apropiadas en ambos autos, anteriormente, ambos autos se dañaban de la misma manera<br />
</li>
<li>Se arregló un crash ocasional que ocurría al estar parado al lado de las escaleras mecánicas<br />
</li>
<li>Se solucionó un problema de IA donde los enemigos se volvían demasiado precisos después de que el jugador había estado antes en el auto<br />
</li>
<li>IMGs mayor a 4GB ahora se manejan adecuadamente<br />
</li>
<li>Alt+F4 funciona correctamente<br />
</li>
<li>Varios vehículos ahora tienen componentes animados adicionales: <a href="https://www.youtube.com/watch?v=ZLvFfPGG32o" target="_blank" rel="noopener" class="mycode_url">https://www.youtube.com/watch?v=ZLvFfPGG32o</a><br />
</li>
