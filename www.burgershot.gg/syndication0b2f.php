			<description><![CDATA[<a href="https://github.com/ZanderOperator/open.RP" target="_blank" rel="noopener" class="mycode_url">open.RP GitHub repository</a><br />
<br />
Gamemode is 10% translated from ex-Yu(Croatian, Serbian, Bosnian), database is normalized with Codd standards, statements are completely rewritten(there are few old mysql_query left), it uses sampctl, and it has only 2 legacy libraries left. <br />
<br />
It consists of modules, the structure is very well palpable from main.pwn<br />
<br />
It has many unique systems such as inactive account checkup(checks if user has less than (5 or 10, don't remember exactly) hours of play per month, or if user hasn't logged in for a month, and it strips him from houses, apartments, businesses, and complexes. <br />
<br />
Also, it has 2 basketball courts with basketball feature, it counts 2 or 3 points per score, depending on the distance you shot from.<br />
<br />
Pretty much everything is very dynamic: houses, garages, businesses, vehicles; and admin commands are flawlessly developed. <br />
<br />
